#include "expan/expan_tokenizer.h"

void				expan_tokenizer_addone(t_expan_tokenizer *this)
{
	this->to_push[this->to_push_index] = this->input[this->input_index];
	this->input_index += 1;
	this->to_push_index += 1;
}
