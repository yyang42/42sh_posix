#include "expan/expansion.h"

t_expansion			*expansion_new_from_token(t_token *to_expand)
{
	t_expansion		*new;

	new = expansion_new();
	new->split = expan_tokenizer_tokenize(to_expand->text_unexpanded);
	return (new);
}
