#include "edit/cursor.h"

void				cursor_reset_pos(void)
{

	t_cursor		*cursor;

	cursor = cursor_singleton();
	while (cursor->current_cursor_pos > 0)
	{
		terminal_delete_line();
		terminal_cursor_up();
		cursor->current_cursor_pos--;
	}
}
